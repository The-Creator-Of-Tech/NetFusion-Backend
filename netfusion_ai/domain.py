@dataclass
class AIReport:
    """Comprehensive AI Investigation Report."""
    report_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    investigation_id: str = ""
    title: str = "AI Security Investigation Report"
    generated_at: float = field(default_factory=time.time)
    executive_summary: str = ""
    technical_summary: str = ""
    incident_narrative: str = ""
    timeline_narrative: str = ""
    evidence_narrative: str = ""
    mitre_findings: List[MITREInference] = field(default_factory=list)
    hypotheses: List[Hypothesis] = field(default_factory=list)
    risk_assessment: Optional[RiskScore] = None
    recommendations: Dict[str, List[RecommendationItem]] = field(default_factory=dict)
    appendices: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        rec_dict = {}
        for cat, items in self.recommendations.items():
            rec_dict[cat] = [i.to_dict() for i in items]
        return {
            "report_id": self.report_id,
            "investigation_id": self.investigation_id,
            "title": self.title,
            "generated_at": self.generated_at,
            "executive_summary": self.executive_summary,
            "technical_summary": self.technical_summary,
            "incident_narrative": self.incident_narrative,
            "timeline_narrative": self.timeline_narrative,
            "evidence_narrative": self.evidence_narrative,
            "mitre_findings": [m.to_dict() for m in self.mitre_findings],
            "hypotheses": [h.to_dict() for h in self.hypotheses],
            "risk_assessment": self.risk_assessment.to_dict() if self.risk_assessment else None,
            "recommendations": rec_dict,
            "appendices": self.appendices,
        }


