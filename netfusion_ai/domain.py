"""
NetFusion AI Domain Models
Enterprise dataclass representations for explainable AI outputs, hypotheses,
risk metrics, confidence metadata, recommendations, MITRE reasoning, reports, and safety claims.
"""

import time
import uuid
from dataclasses import dataclass, field, asdict
from typing import Any, Dict, List, Optional

from netfusion_workflow.enums import Priority, BusinessImpact, Likelihood
from .enums import (
    AnalysisCategory,
    ClaimType,
    ConfidenceLevel,
    RecommendationCategory,
    TacticalPhase,
)


@dataclass
class EvidenceReference:
    """Structured link to underlying evidence or canonical object."""
    evidence_id: str
    source_type: str  # e.g., "sysmon", "nmap", "tshark", "threat_intel", "timeline"
    summary: str
    relevance_score: float = 1.0
    timestamp: Optional[float] = None
    metadata: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "evidence_id": self.evidence_id,
            "source_type": self.source_type,
            "summary": self.summary,
            "relevance_score": self.relevance_score,
            "timestamp": self.timestamp,
            "metadata": self.metadata,
        }


@dataclass
class Explanation:
    """Structured reasoning output explaining every AI conclusion."""
    evidence_references: List[EvidenceReference] = field(default_factory=list)
    reasoning_summary: str = ""
    confidence: ConfidenceLevel = ConfidenceLevel.MEDIUM
    assumptions: List[str] = field(default_factory=list)
    limitations: List[str] = field(default_factory=list)
    unknowns: List[str] = field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "evidence_references": [ref.to_dict() for ref in self.evidence_references],
            "reasoning_summary": self.reasoning_summary,
            "confidence": self.confidence.value if isinstance(self.confidence, ConfidenceLevel) else self.confidence,
            "assumptions": self.assumptions,
            "limitations": self.limitations,
            "unknowns": self.unknowns,
        }


@dataclass
class Claim:
    """Classified assertion generated by AI engines for safety verification."""
    statement: str
    claim_type: ClaimType = ClaimType.INFERENCE
    supporting_evidence: List[EvidenceReference] = field(default_factory=list)
    is_verified: bool = True
    notes: str = ""

    def to_dict(self) -> Dict[str, Any]:
        return {
            "statement": self.statement,
            "claim_type": self.claim_type.value if isinstance(self.claim_type, ClaimType) else self.claim_type,
            "supporting_evidence": [e.to_dict() for e in self.supporting_evidence],
            "is_verified": self.is_verified,
            "notes": self.notes,
        }


